int	is_sorted(long *tab, int nb_nb)
{
	int	i;
	int	j;

	i = 0;
	while (i < nb_nb)
	{
		j = i;
		while (j < nb_nb)
		{
			if (tab[i] > tab[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
