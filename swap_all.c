char	*swap_all(long *a, long *b)
{
	long	temp_a;
	long	temp_b;

	temp_a = a[0];
	a[0] = a[1];
	a[1] = temp_a;

	temp_b = b[0];
	b[0] = b[1];
	b[1] = temp_b;
	return ("ss");
}
