#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t	i;
	int		c1;
	int		c2;

	i = 0;
	while (i < n && s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
	{
		i++;
	}
	c1 = s1[i] % 256;
	c2 = s2[i] % 256;
	if (c1 < 0)
		c1 += 256;
	if (c2 < 0)
		c2 += 256;
	if (i == n)
		return (0);
	return (c1 - c2);
}
